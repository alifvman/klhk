     * @return \Illuminate\View\View
     */
    public function stasiun_air(Request $request)
    {

        if ($request->ajax()) {
            $data = StasiunAir::select('id','name','code')->get();
            return Datatables::of($data)->addIndexColumn()
                ->addColumn('action', function($row){
                    $btn = '<a href="javascript:void(0)" class="btn btn-primary btn-sm">View</a>';
                    return $btn;
                })
                ->rawColumns(['action'])
                ->make(true);
        }

        return view('pages.stasiun_air');

    }

